using System;

class Program
{
    static void Main()
    {
        // Запрашиваем у пользователя максимальную сумму
        Console.Write("Введите максимальную сумму: ");
        int maxSum = int.Parse(Console.ReadLine()); // Преобразуем введенное значение в целое число

        // Создаем объект для генерации случайных чисел
        Random rand = new Random();

        // Инициализируем переменные: сумма (sum) и количество сгенерированных чисел (count)
        int sum = 0;
        int count = 0;

        // Цикл выполняется до тех пор, пока сумма не станет больше максимальной суммы
        while (sum <= maxSum)
        {
            // Генерируем случайное число от 1 до 9 включительно
            int nextValue = rand.Next(1, 10);

            // Проверяем, превысит ли добавление нового числа максимальную сумму
            if (sum + nextValue > maxSum) break; // Если сумма превысит, прерываем цикл

            // Добавляем сгенерированное число к общей сумме
            sum += nextValue;

            // Увеличиваем счетчик чисел
            count++;

            // Выводим сгенерированное число
            Console.WriteLine(nextValue);
        }

        // Выводим количество чисел, которые были добавлены до достижения максимальной суммы
        Console.WriteLine($"Количество элементов: {count}");
    }
}